  const tlPredchoziD = document.querySelector('#tlPredchoziD');
  const tlNasledujiciD = document.querySelector('#tlNasledujiciD');
  const tlPredchoziA = document.querySelector('#tlPredchoziA');
  const tlNasledujiciA = document.querySelector('#tlNasledujiciA');
  const tlPredchoziF = document.querySelector('#tlPredchoziF');
  const tlNasledujiciF = document.querySelector('#tlNasledujiciF');
  
  
  const sectionDrama = document.querySelector('#section-drama');
  const sectionAnime = document.querySelector('#section-anime');
  const sectionFilmy = document.querySelector('#section-filmy');
  
  let indexDrama = 0;
  let indexAnime = 0;
  let indexFilmy = 0;
  
  vypis(indexDrama, asijskeDrama, sectionDrama);
  vypis(indexAnime, anime, sectionAnime);
  vypis(indexFilmy, filmy, sectionFilmy);
  
  tlPredchoziD.addEventListener('click', function(){
    if(indexDrama === 0){
      indexDrama = asijskeDrama.length - 1;
    }else{
      indexDrama--;
    }
    vypis(indexDrama, asijskeDrama, sectionDrama);
  });
  
  tlNasledujiciD.addEventListener('click', function(){
    if(indexDrama === asijskeDrama.length - 1){
      indexDrama = 0;
    }else{
      indexDrama++;
    }
    vypis(indexDrama, asijskeDrama, sectionDrama);
  });
  
  tlPredchoziA.addEventListener('click', function(){
    if(indexAnime === 0){
      indexAnime = anime.length - 1;
    }else{
      indexAnime--;
    }
    vypis(indexAnime, anime, sectionAnime);
  });
  
  tlNasledujiciA.addEventListener('click', function(){
    if(indexAnime === anime.length - 1){
      indexAnime = 0;
    }else{
      indexAnime++;
    }
    vypis(indexAnime, anime, sectionAnime);
  });
  
  tlPredchoziF.addEventListener('click', function(){
    if(indexFilmy === 0){
      indexFilmy = filmy.length - 1;
    }else{
      indexFilmy--;
    }
    vypis(indexFilmy, filmy, sectionFilmy);
  });
  
  tlNasledujiciF.addEventListener('click', function(){
    if(indexFilmy === filmy.length - 1){
      indexFilmy = 0;
    }else{
      indexFilmy++;
    }
    vypis(indexFilmy, filmy, sectionFilmy);
  });
  
  
  function vypis(index, data, sekce){
    const elNazev = sekce.querySelector('.nazev');
    const elPopisek = sekce.querySelector('.popisek');
    const elVideo = sekce.querySelector('.video');
  
    elNazev.textContent = data[index].nazev;
    elPopisek.textContent = data[index].popis;
    elVideo.setAttribute('src', data[index].video);
  }
  